      <div class="col-lg-12">
        <h2 class="hero-title mb-3">Project Execution & Challenges</h2>

        <h3 class="brand-title">Challenges</h3>
        <ul class="brand-list mb-4">
          <li class="mb-3">
            <strong>Architectural Sensitivity:</strong> The showroom’s facade featured intricate cladding and design lines. The sign board had to be mounted without disturbing the original structure or visible wiring. This required custom-designed brackets and hidden mounting mechanisms.
          </li>
          <li class="mb-3">
            <strong>Lighting Precision:</strong> Tanishq’s brand color palette demanded a very specific shade of illumination. Achieving that soft golden hue required multiple rounds of LED temperature testing and diffuser adjustments.
          </li>
          <li class="mb-3">
            <strong>Urban Installation Constraints:</strong> The store was located in a high-footfall retail area, which limited installation hours. The team coordinated night-time operations to ensure smooth completion without disrupting store activity or customer experience.
          </li>
          <li>
            <strong>Surface Variation:</strong> The uneven wall texture posed alignment challenges. The team used a combination of laser marking and modular frame sections to maintain flawless symmetry.
          </li>
        </ul>

        <h3 class="brand-title">Solutions & Approach</h3>
        <ul class="brand-list mb-4">
          <li class="mb-3">
            <strong>Custom Fabrication:</strong> Each letter of the Tanishq logo was individually fabricated with precision CNC cutting and handcrafted polishing to maintain brand authenticity.
          </li>
          <li class="mb-3">
            <strong>Hidden Wiring System:</strong> Brand Signages implemented an internal conduit system to keep all wiring concealed, resulting in a clean, seamless finish.
          </li>
          <li class="mb-3">
            <strong>Temperature-Controlled LEDs:</strong> Warm-white, low-glare LEDs were used to replicate the subtle luxury of the brand’s tone.
          </li>
          <li>
            <strong>Modular Installation:</strong> The signage was pre-assembled in sections at Brand Signages’ facility, reducing on-site time and ensuring installation accuracy.
          </li>
        </ul>

        <h3 class="brand-title">Outcome</h3>
        <ul class="brand-list mb-4">
          <li class="mb-3">
            The final installation brought the showroom’s exterior to life — an understated yet powerful visual statement. The illuminated Tanishq logo glows elegantly against the store faade, visible from afar but soft on the eyes up close.
          </li>
          <li class="mb-3">
            The sign board amplified the store’s visibility, especially during evening hours.
          </li>
          <li class="mb-3">
            The warm illumination added a luxurious ambience consistent with Tanishq’s in-store experience.
          </li>
          <li>
            The signage’s durable build ensures it will retain its charm despite exposure to weather and time.
          </li>
